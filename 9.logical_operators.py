# logical operators(if and/not/or)

# and--------------------------------------------------------------------------------------------------------

temp =40

if temp<=0 and temp>=30:# and aint working
    print("the tem is bad")
else:
    print("the tem is good")

# or--------------------------------------------------------------------------------------------------------

temp =40

if temp<=0 or temp>=30:
    print("the tem is bad")
else:
    print("the tem is good")

# not----------------------------------------------------------------------------------------------------------

temp =40

if not temp :
    print("the tem is bad")
else:
    print("the tem is good")